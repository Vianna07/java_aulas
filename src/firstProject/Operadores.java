package firstProject;

public class Operadores {
	public static void main(String[] args) {
		double num = 9.78d;
		int x = -10;
		x += 5;
		
		System.out.println(x + "\n");
		System.out.println(-x);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++x + "\n");
		System.out.println((int) num);
	}
}
